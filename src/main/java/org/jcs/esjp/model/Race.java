package org.jcs.esjp.model;

import java.awt.Image;

public class Race
{
    private String name;
    private Image image;

    public String getName()
    {
        return name;
    }
    public void setName(final String name)
    {
        this.name = name;
    }
    public Image getImage()
    {
        return image;
    }
    public void setImage(final Image image)
    {
        this.image = image;
    }
}
